// O que são operadores em Javascript?
// São simbolos especiais que envolvem um ou mais operandos com a finalidade de produzir um determinado resultado

//OPERADOR DE ATRIBUIÇÃO
//O operador de atribuição básico é o sinal de igual (=)
// Ele atribui um valor a uma variável:

var x; // declaração da variavel X

x = 5; // Essa operação atribui a X o valor 5
console.log(x);

console.log('-------------------02--------------------');
//OPERADOR DE ATRIBUIÇÃO COMPOSTA COM ADIÇÃO (+=)

x += 10; 

console.log(x);

console.log('-------------------03--------------------');
//OPERADOR DE ATRIBUIÇÃO COMPOSTA COM SUBTRAÇÃO (-=)

x -= 2;
console.log(x);

console.log('-------------------04--------------------');
//OPERADOR DE ATRIBUIÇÃO COMPOSTA COM MULTIPLICAÇÃO (*=)

x *= 2;
console.log(x);

console.log('-------------------05--------------------');
//OPERADOR DE ATRIBUIÇÃO COMPOSTA COM DIVISÃO (/=)

x /= 3;
console.log(x);

console.log('-------------------06--------------------');

//OPERADOR DE ATRIBUIÇÃO COMPOSTA COM RESTO DA DIVISÃO (%=)

x %= 2;
console.log(x);